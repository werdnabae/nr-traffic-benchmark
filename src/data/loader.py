"""
NetworkData: loads and prepares all data for a single traffic network.

This is the single source of truth for all arrays consumed by the Dataset,
models, and evaluation code.  It does NOT depend on the Anomaly Labeling
project at runtime — NR labels must be pre-generated by running:

    python scripts/generate_nr_labels.py

After construction the following numpy arrays are available (float32 unless
noted otherwise):

Time-aligned  (shape T × N)
────────────────────────────
speed_raw       original speed in mph (may contain NaN)
speed_norm      per-link z-normalised speed  ((speed − μ) / σ)
full_nr         binary NR labels [0/1]
causal_fixed    rolling min_dur_steps-confirmation causal label [0/1]
observation_time earliest timestep index at which full_nr[t,n] becomes
                causally observable; np.inf for non-NR steps  (float64)
incidents       binary Waze incident indicator [0/1]
v_rec           recurrent lower-bound speed in mph

Time-aligned  (shape T × W)
────────────────────────────
weather         normalised weather features (W varies by network)

1-D arrays
──────────
session_ids     (T,) int64 — new session at overnight/weekend gaps
speed_mean      (N,) per-link training mean (mph)
speed_std       (N,) per-link training std  (mph)
links           list[str] of N TMC codes
weather_cols    list[str] of W weather column names

Split information
─────────────────
split_mask      dict{'train':bool(T), 'val':bool(T), 'test':bool(T)}
timestamps      pd.DatetimeIndex of length T
"""

from __future__ import annotations

import warnings
from pathlib import Path

import numpy as np
import pandas as pd
import yaml


# ─────────────────────────────────────────────────────────────────────────────
# Session ID helper (copied from ensemble_labeling.py to avoid runtime dep.)
# ─────────────────────────────────────────────────────────────────────────────


def _compute_session_ids(index: pd.DatetimeIndex, interval_min: int = 5) -> np.ndarray:
    """Assign integer session IDs; new session when gap > 2 × interval_min."""
    threshold = pd.Timedelta(minutes=2 * interval_min)
    diffs = pd.Series(index).diff()
    new_sess = (diffs > threshold).to_numpy().copy()
    new_sess[0] = True
    return new_sess.cumsum().astype(np.int64)


# ─────────────────────────────────────────────────────────────────────────────
# Causal label helpers
# ─────────────────────────────────────────────────────────────────────────────


def compute_causal_fixed(full_nr: np.ndarray, min_dur_steps: int = 3) -> np.ndarray:
    """
    Compute the rolling-confirmation causal label.

    causal_fixed[t, n] = 1  iff  full_nr[t-k:t+1, n] are all 1
    where k = min_dur_steps - 1.

    This is a fixed (T, N) array used for:
      • transition regime classification at the last context step
      • loss weighting on the target window
    """
    T, N = full_nr.shape
    causal = np.zeros((T, N), dtype=np.float32)
    w = min_dur_steps
    for t in range(w - 1, T):
        causal[t] = full_nr[t - w + 1 : t + 1].prod(axis=0).astype(np.float32)
    return causal


def compute_observation_time(full_nr: np.ndarray, min_dur_steps: int = 3) -> np.ndarray:
    """
    Precompute the earliest timestep index T at which full_nr[t, n] becomes
    causally observable in a sliding window ending at T.

    For a step t inside an NR episode starting at ep_start:
      observation_time[t, n] = max(ep_start + min_dur_steps - 1, t)

    Interpretation:
      • First (min_dur_steps-1) steps of each episode share the same
        observation_time = ep_start + min_dur_steps - 1  (back-fill window).
      • All later steps in the episode are confirmed at their own time.
      • Non-NR steps: observation_time = np.inf.

    Used by the Dataset to compute the context-dependent back-filled causal
    label for Config 4 (speed + nr_causal).
    """
    T, N = full_nr.shape
    obs = np.full((T, N), np.inf, dtype=np.float64)
    w = min_dur_steps

    for n in range(N):
        col = full_nr[:, n]
        t = 0
        while t < T:
            if col[t] == 1:
                ep_start = t
                while t < T and col[t] == 1:
                    t += 1
                ep_end = t - 1
                confirmation = ep_start + w - 1
                for s in range(ep_start, ep_end + 1):
                    obs[s, n] = float(max(confirmation, s))
            else:
                t += 1

    return obs


# ─────────────────────────────────────────────────────────────────────────────
# Main class
# ─────────────────────────────────────────────────────────────────────────────


class NetworkData:
    """Load and prepare all data for a single traffic network."""

    INTERVAL_MIN = 5  # data resolution

    def __init__(
        self,
        network: str,
        config: dict,
        train_frac: float = 0.70,
        val_frac: float = 0.15,
        min_dur_steps: int = 3,
    ) -> None:
        """
        Parameters
        ----------
        network      : "tsmo" or "cranberry"
        config       : parsed config.yaml dict (or the sub-dict for the network)
        train_frac   : fraction of timesteps for training (chronological)
        val_frac     : fraction of timesteps for validation
        min_dur_steps: rolling confirmation window for causal labels (steps)
        """
        self.network = network.lower()
        self.config = config
        self.train_frac = train_frac
        self.val_frac = val_frac
        self.min_dur_steps = min_dur_steps

        self._root = Path(__file__).resolve().parent.parent.parent  # project root

        # ── build in order ────────────────────────────────────────────────────
        self._load_speed_incidents()
        self._load_nr_labels()
        self._load_weather()
        self._align_common_index()
        self._compute_session_ids()
        self._split()
        self._normalise_speed()
        self._normalise_weather()
        self._compute_causal_arrays()

        print(
            f"[NetworkData] {self.network.upper()}  "
            f"T={len(self.timestamps)}  N={len(self.links)}  "
            f"W={len(self.weather_cols)}  "
            f"NR rate={100 * self.full_nr.mean():.2f}%"
        )

    # ─────────────────────────────────────────────────────────────────────────
    # Private helpers
    # ─────────────────────────────────────────────────────────────────────────

    def _resolve(self, rel: str) -> Path:
        p = Path(rel)
        return p if p.is_absolute() else (self._root / p).resolve()

    def _pcfg(self) -> dict:
        """Return the per-network path sub-dict from config."""
        return self.config["paths"][self.network]

    # ── loading ───────────────────────────────────────────────────────────────

    def _load_speed_incidents(self) -> None:
        pcfg = self._pcfg()
        speed_path = self._resolve(pcfg["speed"])
        incidents_path = self._resolve(pcfg["incidents"])

        speed_df = pd.read_parquet(speed_path)
        speed_df.index = pd.to_datetime(speed_df.index)
        speed_df = speed_df.sort_index()

        inc_df = pd.read_parquet(incidents_path)
        inc_df.index = pd.to_datetime(inc_df.index)
        inc_df = inc_df.sort_index()

        # Keep only numeric link columns (drop any aggregate columns)
        speed_df = speed_df.select_dtypes(include=[np.number])
        inc_df = inc_df.select_dtypes(include=[np.number])

        self._speed_df = speed_df
        self._incident_df = inc_df
        self.links = list(speed_df.columns.astype(str))

    def _load_nr_labels(self) -> None:
        pcfg = self._pcfg()
        nr_path = self._resolve(pcfg["nr_labels"])
        vrec_path = self._resolve(pcfg["v_rec"])

        if not nr_path.exists():
            raise FileNotFoundError(
                f"NR labels not found at:\n  {nr_path}\n"
                f"Run:  python scripts/generate_nr_labels.py --network {self.network}"
            )

        nr_df = pd.read_parquet(nr_path)
        vrec_df = pd.read_parquet(vrec_path)
        nr_df.index = pd.to_datetime(nr_df.index)
        vrec_df.index = pd.to_datetime(vrec_df.index)

        # Do NOT reindex to speed columns here — _align_common_index() will
        # intersect columns and restrict to the freeway-only NR-labelled links.

        self._nr_df = nr_df
        self._vrec_df = vrec_df

    def _load_weather(self) -> None:
        pcfg = self._pcfg()
        wpath = self._resolve(pcfg["weather"])

        if not wpath.exists():
            warnings.warn(
                f"Weather file not found: {wpath}. "
                "Weather features will be unavailable."
            )
            self._weather_df = pd.DataFrame(index=self._speed_df.index)
            return

        try:
            weather_df = pd.read_pickle(wpath)
        except Exception as e:
            warnings.warn(f"Could not load weather file {wpath}: {e}")
            self._weather_df = pd.DataFrame(index=self._speed_df.index)
            return

        weather_df.index = pd.to_datetime(weather_df.index)
        weather_df = weather_df.sort_index()

        # Keep only numeric columns (drop categorical like weather_condition)
        weather_df = weather_df.select_dtypes(include=[np.number])

        self._weather_df = weather_df

    # ── alignment ─────────────────────────────────────────────────────────────

    def _align_common_index(self) -> None:
        """
        Align all DataFrames on a common time index AND a common column set.

        The NR labels were generated only for freeway links (228 TSMO, 78
        Cranberry).  The raw speed parquet contains all links (~1400 TSMO).
        We restrict to the intersection so every link has NR label coverage.
        """
        # ── align time index ──────────────────────────────────────────────────
        idx = self._speed_df.index
        for df in [self._nr_df, self._vrec_df]:
            idx = idx.intersection(df.index)
        if len(self._weather_df.columns) > 0:
            idx = idx.intersection(self._weather_df.index)
        idx = idx.sort_values()

        # ── align link columns: restrict to NR-labelled freeway links ─────────
        # (NR labels only exist for the ~228 / ~78 freeway TMCs selected by
        #  the ensemble labeler; non-freeway links have no NR ground truth.)
        nr_cols = self._nr_df.columns.astype(str)
        speed_cols = self._speed_df.columns.astype(str)
        # Use only links present in BOTH speed and NR data, preserving NR order
        common_cols = [c for c in nr_cols if c in set(speed_cols)]
        if len(common_cols) == 0:
            raise ValueError(
                "No overlapping TMC columns between speed data and NR labels. "
                "Check that generate_nr_labels.py was run for this network."
            )

        self._speed_df = self._speed_df.reindex(index=idx, columns=common_cols)
        self._incident_df = self._incident_df.reindex(
            index=idx, columns=common_cols
        ).fillna(0)
        self._nr_df = self._nr_df.reindex(index=idx, columns=common_cols).fillna(0)
        self._vrec_df = self._vrec_df.reindex(index=idx, columns=common_cols).ffill()

        if len(self._weather_df.columns) > 0:
            self._weather_df = self._weather_df.reindex(idx).ffill().bfill()
        else:
            self._weather_df = pd.DataFrame(index=idx)

        self.timestamps = idx
        self.links = common_cols  # update link list to freeway-only

    # ── session IDs ───────────────────────────────────────────────────────────

    def _compute_session_ids(self) -> None:
        self.session_ids = _compute_session_ids(
            pd.DatetimeIndex(self.timestamps), self.INTERVAL_MIN
        )

    # ── 70/15/15 split ────────────────────────────────────────────────────────

    def _split(self) -> None:
        T = len(self.timestamps)
        t_train = int(T * self.train_frac)
        t_val = int(T * (self.train_frac + self.val_frac))

        mask_train = np.zeros(T, dtype=bool)
        mask_val = np.zeros(T, dtype=bool)
        mask_test = np.zeros(T, dtype=bool)
        mask_train[:t_train] = True
        mask_val[t_train:t_val] = True
        mask_test[t_val:] = True

        self.split_mask = {
            "train": mask_train,
            "val": mask_val,
            "test": mask_test,
        }

    # ── normalisation ─────────────────────────────────────────────────────────

    def _normalise_speed(self) -> None:
        speed = self._speed_df.values.astype(np.float32)  # (T, N)
        train_speed = speed[self.split_mask["train"]]

        # Per-link statistics (ignore NaN)
        with warnings.catch_warnings():
            warnings.simplefilter("ignore", RuntimeWarning)
            self.speed_mean = np.nanmean(train_speed, axis=0).astype(np.float32)
            self.speed_std = np.nanstd(train_speed, axis=0).astype(np.float32)

        # Prevent division by zero
        self.speed_std = np.where(self.speed_std < 1e-3, 1.0, self.speed_std)

        # Fill NaN with link mean before normalising
        for n in range(speed.shape[1]):
            nan_mask = np.isnan(speed[:, n])
            if nan_mask.any():
                speed[nan_mask, n] = self.speed_mean[n]

        self.speed_raw = speed.copy()
        self.speed_norm = (
            (speed - self.speed_mean[np.newaxis, :]) / self.speed_std[np.newaxis, :]
        ).astype(np.float32)

    def _normalise_weather(self) -> None:
        if len(self._weather_df.columns) == 0:
            self.weather = np.zeros((len(self.timestamps), 0), dtype=np.float32)
            self.weather_cols = []
            return

        weather = self._weather_df.values.astype(np.float32)  # (T, W)
        self.weather_cols = list(self._weather_df.columns)

        train_weather = weather[self.split_mask["train"]]
        w_mean = np.nanmean(train_weather, axis=0).astype(np.float32)
        w_std = np.nanstd(train_weather, axis=0).astype(np.float32)
        w_std = np.where(w_std < 1e-6, 1.0, w_std)

        # Fill NaN
        for w in range(weather.shape[1]):
            nan_mask = np.isnan(weather[:, w])
            if nan_mask.any():
                weather[nan_mask, w] = w_mean[w]

        self.weather = (
            (weather - w_mean[np.newaxis, :]) / w_std[np.newaxis, :]
        ).astype(np.float32)

    # ── causal arrays ─────────────────────────────────────────────────────────

    def _compute_causal_arrays(self) -> None:
        nr = self._nr_df.values.astype(np.float32)
        inc = self._incident_df.values.astype(np.float32)
        vrec = self._vrec_df.values.astype(np.float32)

        self.full_nr = nr
        self.incidents = inc
        self.v_rec = vrec

        self.causal_fixed = compute_causal_fixed(nr, self.min_dur_steps)
        self.observation_time = compute_observation_time(nr, self.min_dur_steps)

    # ─────────────────────────────────────────────────────────────────────────
    # Convenience
    # ─────────────────────────────────────────────────────────────────────────

    @property
    def T(self) -> int:
        return len(self.timestamps)

    @property
    def N(self) -> int:
        return len(self.links)

    @property
    def W(self) -> int:
        return self.weather.shape[1]

    def summary(self) -> dict:
        """Return a dict of basic dataset statistics."""
        nr_rate_train = self.full_nr[self.split_mask["train"]].mean()
        nr_rate_val = self.full_nr[self.split_mask["val"]].mean()
        nr_rate_test = self.full_nr[self.split_mask["test"]].mean()
        return dict(
            network=self.network,
            T=self.T,
            N=self.N,
            W=self.W,
            period_start=str(self.timestamps[0]),
            period_end=str(self.timestamps[-1]),
            n_sessions=int(self.session_ids[-1]),
            nr_rate_train=round(float(nr_rate_train) * 100, 2),
            nr_rate_val=round(float(nr_rate_val) * 100, 2),
            nr_rate_test=round(float(nr_rate_test) * 100, 2),
        )


# ─────────────────────────────────────────────────────────────────────────────
# Factory
# ─────────────────────────────────────────────────────────────────────────────


def load_network(
    network: str,
    config_path: str | Path | None = None,
) -> NetworkData:
    """
    Convenience factory: load config and return a NetworkData object.

    Parameters
    ----------
    network     : "tsmo" or "cranberry"
    config_path : path to config.yaml; defaults to <project_root>/config.yaml
    """
    if config_path is None:
        config_path = Path(__file__).resolve().parent.parent.parent / "config.yaml"

    with open(config_path) as f:
        config = yaml.safe_load(f)

    data_cfg = config.get("data", {})
    return NetworkData(
        network=network,
        config=config,
        train_frac=data_cfg.get("train_frac", 0.70),
        val_frac=data_cfg.get("val_frac", 0.15),
        min_dur_steps=data_cfg.get("min_dur_steps", 3),
    )
